package com.example.user.User.dto;

public class BooleanDTO {

    public BooleanDTO() {
    }

    private boolean status;

    public BooleanDTO(boolean status) {
        this.status = status;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }



}
